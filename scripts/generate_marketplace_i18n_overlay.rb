  source_entry = existing.fetch(agent_id, {})
  source_hans = sanitized_existing_fields(agent, source_entry.fetch("zh-Hans", {}))
  source_hant = sanitized_existing_fields(agent, source_entry.fetch("zh-Hant", {}))
  base_hans = generated_hans(agent)
  hans = base_hans.merge(source_hans)
  hant_base = hans.transform_values { |value| to_hant(value) }
  hant = hant_base.merge(source_hant)
  extras = source_entry.reject { |locale, _| ["zh-Hans", "zh-Hant"].include?(locale) }

  generated[agent_id] = {
    "zh-Hans" => hans,
    "zh-Hant" => hant
  }.merge(extras)
end

