HANT_CHARS = HANT_PAIRS.to_h { |pair| pair.each_char.to_a }.freeze

def to_hant(text)
  text.each_char.map { |char| HANT_CHARS.fetch(char, char) }.join
end

def translate_phrase(text)
  value = text.to_s.dup
  (PHRASES_ZH.merge(VERB_ZH).keys.sort_by { |key| -key.length }).each do |english|
    value = value.gsub(english, PHRASES_ZH[english] || VERB_ZH[english])
  end
  value = value.gsub(" and ", "和")
               .gsub(" with ", "与")
               .gsub(" for ", "用于")
               .gsub(" across ", "覆盖")
               .gsub(" through ", "通过")
               .gsub(" into ", "转化为")
               .gsub(" from ", "从")
               .gsub(" to ", "到")
               .gsub(" of ", "的")
               .gsub(" in ", "在")
               .gsub(" & ", "与")
               .gsub(", ", "、")
               .gsub(" - ", " - ")
               .strip
  value.empty? ? "相关专业能力" : value
end

def mostly_english?(text)
  compact = text.to_s.gsub(/\s+/, "")
  return false if compact.empty?

  english_chars = compact.scan(/[A-Za-z]/).length
  cjk_chars = compact.scan(/\p{Han}/).length
  english_chars > 0 && cjk_chars.zero?
end

def sanitized_existing_fields(agent, fields)
  fields.each_with_object({}) do |(key, value), cleaned|
    next unless value.is_a?(String)
    next if value.strip.empty?
    next if value == agent[key]
    next if key != "name" && mostly_english?(value)

    cleaned[key] = value
  end
end

def generated_hans(agent)
  name = NAMES_ZH.fetch(agent.fetch("name"), agent.fetch("name"))
  division = DIVISIONS_ZH.fetch(agent.fetch("division"), agent.fetch("division"))
  specialty = translate_phrase(agent["specialty"] || agent.fetch("name"))
  when_to_use = translate_phrase(agent["whenToUse"] || agent["description"] || agent.fetch("name"))
  when_to_use = "处理#{name}相关任务" if mostly_english?(when_to_use)

  {
    "name" => name,
    "division" => division,
    "description" => "#{name}专注于#{specialty}，适合#{when_to_use}。帮助你把复杂需求拆解为清晰、可执行的方案。",
    "vibe" => "聚焦#{specialty}，把复杂问题转化为清晰行动。",
    "specialty" => specialty,
    "whenToUse" => when_to_use
  }
end

